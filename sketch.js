function sum(a, b){
    return a + b;
}

function prod(a, b){
    return a * b;
}

function div(a, b){
    return a / b;
}

function sub(a, b){
    return a - b;
}

function three(a, b, c){
    return a * b * c + 42;
}

module.exports = {
    sum: sum,
    prod: prod,
    div: div,
    sub: sub,
    three, three,
};